package abrackgh.movieAPI.movies;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;
import org.bson.types.ObjectId;
import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;
import org.springframework.data.mongodb.core.mapping.DocumentReference;

import java.util.List;

@Document(collection = "movies")
@Data //negates need for constructor, getter, setter
@AllArgsConstructor //creates constructor that takes all fields as arguments
@NoArgsConstructor //constructor takes no arguments
public class Movie {

    @Id
    private ObjectId id;
    private String imdbId;
    private String title;
    private String releaseDate;
    private String trailerLink;
    private String poster;
    private List<String> genres;
    private List<String> backdrops;
    @DocumentReference //store only Ids of review and reviews stored in separate collection. Manual reference relationship
    private List<Review> reviewIds; //embedded relationship



}
